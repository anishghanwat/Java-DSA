public class UranaryOperators {
    public static void main(String args[]) {
        int A = 10;
        int B = A;
        System.out.println(B);
        System.out.println(++B);
        System.out.println("----------");
        System.out.println(B);
        System.out.println(--B);
        System.out.println("----------");
        System.out.println(B);
        System.out.println(B++);
        System.out.println("----------");
        System.out.println(B);
        System.out.println(B--);
    }
}