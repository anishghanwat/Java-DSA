import java.util.*;

public class mathclass {
    public static void main(String[] args) {
        int x = 28;
        int y = 4;

        System.out.println("Minimun of " + x + " and " + y + " is " + Math.min(x, y));
        System.out.println("Maximum of " + x + " and " + y + " is " + Math.max(x, y));
        System.out.println("Square root of " + x + " is " + Math.sqrt(x) + " and Square root of " + y + " is " + Math.sqrt(y));
        System.out.println("Pow of " + x + " raised to " + y + " is " + Math.pow(x, y));
        // System.out.println("Average of " + x + " and " + y + " is " + Math.avg(x,
        // y));
        System.out.println("Absolute value of " + x + " is " + Math.abs(x) + " and absolute value of " + y + " is " + Math.abs(y));
    }
}
